int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);

/**
 * add - adds a and b
 * @a: a
 * @b: b
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts a and b
 * @a: a
 * @b: b
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies a and b
 * @a: a
 * @b: b
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides a and b
 * @a: a
 * @b: b
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of a and b
 * @a: a
 * @b: b
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
